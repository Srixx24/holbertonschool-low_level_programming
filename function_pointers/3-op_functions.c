#include "3-calc.h"
#include <stdlib.h>
#include <stdio.h>
/**
 * op_add - adding
 * @a: first value
 * @b: second value
 *
 * return: result
 */


int op_add(int a, int b)
{
	return (a + b);
}

int op_sub(int a, int b)
{
	return (a - b);
}

int op_mul(int a, int b)
{
	return (a * b);
}

int op_div(int a, int b)
{
	return (a / b);
}

int op_mod(int a, int b)
{
	return (a % b);
}
